public class Tasks {

    public static void ForLoop()

    {
        for (int i = 0; i < 5; i++) {
            System.out.println("i love java");
        }
    }

    public static void WhileLoop() {
        int i = 0;
        while(i < 5) {
            System.out.println("I love Java");
            i++;
        }
    }
}


